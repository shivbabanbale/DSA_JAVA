public class relational_oprtar {
    public static void main(String[] args) {
        int a = 10;
        int b = 11;
        System.out.println((a == b));
        System.out.println((a != b));
        System.out.println((a >= b));// check equality also
        System.out.println((a <= b));// check equality also
        System.out.println((a < b));
        System.out.println((a > b));
    }
}
